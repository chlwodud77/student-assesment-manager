
class Subject:
    def setId(self, id):
        self.id = id
    
    def setName(self, name):
        self.name = name
    
    def setParentId(self, parentId):
        self.parentId = parentId

    def getId(self):
        return self.id
    
    def getName(self):
        return self.name
    
    def getParentId(self):
        return self.parentId
